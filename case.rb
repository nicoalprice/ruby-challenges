weather = "lightning"

case weather
when "raining"
	puts "Don't forget your umbrella."	
when "foggy"
	puts "Put on your headlamp!"	
when "lightning"
	puts "Stay inside. It's dangerous out there!"
when "sunny"
	puts "It's a flip-flops kind of day."
when "hailing"
	puts "You need a helmet today."
when "cloudy"
	puts "Bring a rain jacket just in case."
when "humid"
	puts "Put on something you can sweat in."
else
	puts "I dunno. Pack your whole wardrobe, I guess."
end